# Laço de repetição
# -Enquanto a condição for verdadeira, esta condição será executada.
# -Importante definir um critério de parada para que a informação deixe de ser verdadeira e não crie um loop infinito.

numero = -1
while numero < 0:
    numero = int(input('Digite o seu número '))
    
