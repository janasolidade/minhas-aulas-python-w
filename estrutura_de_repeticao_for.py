# -Para cada sequência lógica enquanto o FOR for verdadeiro ele irá continuar sendo executado.
# -Chamado de Loops Infinitos.
# -Executa uma instrução um certo número pré-defiido de vezes.
# -Executa para cada item.

frutas = ['Maçã', 'Banana', 'Uva'] # declarando uma lista
for fruta in frutas: # para cada
    print(fruta)